						if(o=='dongmanmingcheng'){
							this.ruleForm.dongmanmingcheng = obj[o];
							this.ro.dongmanmingcheng = true;
							continue;
						}
						if(o=='fengmianxinxi'){
							this.ruleForm.fengmianxinxi = obj[o];
							this.ro.fengmianxinxi = true;
							continue;
						}
						if(o=='tongzhineirong'){
							this.ruleForm.tongzhineirong = obj[o];
							this.ro.tongzhineirong = true;
							continue;
						}
						if(o=='tongzhiriqi'){
							this.ruleForm.tongzhiriqi = obj[o];
							this.ro.tongzhiriqi = true;
							continue;
						}
